function Readout({ label, value, color, sub }: { label: string; value: string; color: string; sub?: string }) {
  return (
    <div className="rounded-md border px-2.5 py-1.5" style={{ borderColor: BORDER, background: SURFACE }}>
      <div className="font-mono text-[9px] uppercase tracking-wider" style={{ color: MUTED }}>
        {label}
      </div>
      <div className="mt-0.5 truncate font-mono text-sm font-bold tabular-nums" style={{ color }}>
        {value}
      </div>
      {sub && (
        <div className="truncate font-mono text-[9px]" style={{ color: SLATE }}>
          {sub}
        </div>
      )}
    </div>
  );
}
