const xOf = (n: number) => PLOT_L + (Math.log10(Math.max(1, n)) / LOG_NMAX) * PLOT_W;
const yOf = (v: number) => PLOT_B - ((v - Y_LO) / (Y_HI - Y_LO)) * PLOT_H;
const clampV = (v: number) => (v < Y_LO ? Y_LO : v > Y_HI ? Y_HI : v);

/* ------------------------------------------------------------------ */
/*  Derived series                                                     */
/* ------------------------------------------------------------------ */

interface Base {
  mean: Float64Array;
  sig: Float64Array;
}
function buildBase(seed: number): Base {
  const rng = makeRng(seed);
  const mean = new Float64Array(N_MAX + 1);
  const sig = new Float64Array(N_MAX + 1);
  let m = 0;
  let m2 = 0;
  for (let n = 1; n <= N_MAX; n++) {
    const x = MU - H + 2 * H * rng();
    const delta = x - m;
    m += delta / n;
    m2 += delta * (x - m);
    mean[n] = m;
    const v = n > 1 ? m2 / n : 0;
    sig[n] = Math.max(Math.sqrt(v), SIG_FLOOR);
  }
  return { mean, sig };
}

interface Derived {
  csHW: Float64Array;
  clHW: Float64Array;
  failPrefix: Int32Array; // cumulative # of peeks where MU is outside the classical CI
  csMiss: number; // # of peeks where MU is outside the CS (should stay 0)
  csDecN: number; // first n where CS clears the null (lower bound > MU0)
  clDecN: number; // first n where the classical CI clears the null (the p-hacking temptation)
}
function buildDerived(base: Base, alpha: number, z: number): Derived {
  const { mean, sig } = base;
  const csHW = new Float64Array(N_MAX + 1);
  const clHW = new Float64Array(N_MAX + 1);
  const failPrefix = new Int32Array(N_MAX + 1);
  let csMiss = 0;
  let csDecN = 0;
  let clDecN = 0;
  let acc = 0;
  for (let n = 1; n <= N_MAX; n++) {
    const rcs = csRadius(sig[n], n, alpha);
    const rcl = classicalRadius(sig[n], n, z);
    csHW[n] = rcs;
    clHW[n] = rcl;
    const dev = Math.abs(mean[n] - MU);
    if (dev > rcs) csMiss++;
    if (dev > rcl) acc++;
    failPrefix[n] = acc;
    if (csDecN === 0 && mean[n] - rcs > MU0) csDecN = n;
    if (clDecN === 0 && mean[n] - rcl > MU0) clDecN = n;
  }
  return { csHW, clHW, failPrefix, csMiss, csDecN, clDecN };
}

/* stride-decimated sample indices so paths stay light on the log axis */
function decimate(vN: number): number[] {
  const out: number[] = [];
  let lastX = -Infinity;
  for (let n = 1; n <= vN; n++) {
    const x = xOf(n);
    if (n === 1 || n === vN || x - lastX >= 1.1) {
      out.push(n);
      lastX = x;
    }
  }
  return out;
}

